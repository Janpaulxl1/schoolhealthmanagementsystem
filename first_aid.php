    <h1 class="text-center text-xl font-bold mb-6">Basic First Aid Procedures</h1>
    <div class="grid md:grid-cols-2 gap-8 text-sm md:text-base leading-relaxed">
      
      <!-- Left Column -->
      <div>
        <h2 class="font-semibold">1. Wound Care (Minor Cuts and Scrapes)</h2>
        <ul class="list-decimal list-inside pl-4">
          <li>Wash your hands</li>
          <li>Clean the wound with water</li>
          <li>Apply antiseptic (e.g., Betadine)</li>
          <li>Cover with a clean bandage</li>
          <li>Monitor for signs of infection</li>
        </ul>

        <h2 class="font-semibold mt-4">2. Nosebleed</h2>
        <ul class="list-decimal list-inside pl-4">
          <li>Sit the person down and lean them slightly forward</li>
          <li>Pinch the soft part of the nose (nostrils) for 5–10 minutes</li>
          <li>Apply a cold compress on the nose or back of the neck</li>
          <li>Do not tilt the head backward (can cause choking)</li>
        </ul>

        <h2 class="font-semibold mt-4">3. Burns (Minor / First-Degree)</h2>
        <ul class="list-decimal list-inside pl-4">
          <li>Run cool (not cold) water over the burn for 10–15 minutes</li>
          <li>Do not apply ice or toothpaste</li>
          <li>Apply a sterile, non-stick bandage</li>
          <li>Give over-the-counter pain relief if needed</li>
        </ul>

        <h2 class="font-semibold mt-4">4. Choking (Conscious Person)</h2>
        <ul class="list-decimal list-inside pl-4">
          <li>Ask: “Are you choking?”</li>
          <li>Perform the Heimlich maneuver (abdominal thrusts)
            <ul class="list-disc list-inside ml-4">
              <li>Stand behind, place fist above the navel, grab with other hand, and thrust inward-upward</li>
            </ul>
          </li>
          <li>Repeat until object is expelled or person becomes unconscious</li>
        </ul>
      </div>

      <!-- Right Column -->
      <div>
        <h2 class="font-semibold">5. Fainting</h2>
        <ul class="list-decimal list-inside pl-4">
          <li>Lay the person on their back</li>
          <li>Elevate legs (about 12 inches) if no injury is suspected</li>
          <li>Check for breathing</li>
          <li>Loosen tight clothing</li>
          <li>If not waking after 1 minute — call for help</li>
        </ul>

        <h2 class="font-semibold mt-4">6. Sprains (Ankle/Wrist, etc.) – R.I.C.E.</h2>
        <p class="ml-4 mt-1"><strong>R.I.C.E.</strong> stands for:</p>
        <ul class="list-disc list-inside ml-6">
          <li><strong>R</strong>est – avoid movement</li>
          <li><strong>I</strong>ce – apply for 15–20 mins every hour</li>
          <li><strong>C</strong>ompress – wrap with elastic bandage</li>
          <li><strong>E</strong>levate – raise the injured part above heart level</li>
        </ul>

        <h2 class="font-semibold mt-4">7. Seizures</h2>
        <ul class="list-decimal list-inside pl-4">
          <li>Clear the area to prevent injury</li>
          <li>Do NOT restrain or put anything in their mouth</li>
          <li>Place something soft under the head</li>
          <li>After the seizure, roll them onto their side</li>
          <li>Stay with the person and monitor breathing</li>
          <li>Call for medical help if seizure lasts more than 5 minutes</li>
        </ul>

        <h2 class="font-semibold mt-4">8. CPR (Cardiopulmonary Resuscitation) – Basic</h2>
        <p class="ml-4">Only if the person is not breathing:</p>
        <ul class="list-decimal list-inside pl-4">
          <li>Call emergency number</li>
          <li>Place hands in center of chest</li>
          <li>Do 30 chest compressions (2 inches deep, fast pace – ~100–120/min)</li>
          <li>Give 2 rescue breaths if trained</li>
          <li>Repeat until help arrives</li>
        </ul>
